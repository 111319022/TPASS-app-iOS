    // 取得票價 (支援雙向查詢)
    @MainActor func getFare(from start: String, to end: String) -> Int? {
        if start.isEmpty || end.isEmpty { return nil }
        
        let startZH = TYMRTStationData.shared.normalizeStationNameToZH(start)
        let endZH = TYMRTStationData.shared.normalizeStationNameToZH(end)
        if startZH == endZH { return 0 }
        
        // 嘗試1: 查詢 "起點-終點"
        let key1 = "\(startZH)-\(endZH)"
        if let fare = fareDB[key1] { return fare }
        
        // 嘗試2: 查詢 "終點-起點" (反向)
        let key2 = "\(endZH)-\(startZH)"
        if let fare = fareDB[key2] { return fare }
        
        return nil
    }
