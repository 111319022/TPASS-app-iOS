    /// 取得高雄捷運或輕軌票價
    @MainActor func getFare(from startStation: String, to endStation: String) -> Int? {
        // 中文化處理
        let start = KMRTStationData.shared.normalizeStationNameToZH(startStation)
        let end = KMRTStationData.shared.normalizeStationNameToZH(endStation)
        
        // 避免始終站相同
        if start == end { return 0 }
        
        // 查票價表
        if let startFares = fareMatrix[start], let fare = startFares[end] {
            return fare
        }
        
        // 反向查詢（捷運雙向票價相同）
        if let endFares = fareMatrix[end], let fare = endFares[start] {
            return fare
        }
        
        // 若查無資料 (例如輕軌或資料庫未更新)，回傳 nil 或預設值
        return nil
    }
